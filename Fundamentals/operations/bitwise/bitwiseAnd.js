{
    let num1 = 5;
    let num2 = 10;
    let result = num1 & num2;
    console.log("Bitwise AND of", num1, "and", num2, "is:", result);

    let a = 1;
    let b = 2;
    let result2 = a & b;
    console.log("Bitwise AND of", a, "and", b, "is:", result2);

    let x = 12; // In binary: 1100
    let y = 5;  // In binary: 0101
    let result3 = x & y; // In binary: 0100
    console.log("Bitwise AND of", x, "and", y, "is:", result3);
}