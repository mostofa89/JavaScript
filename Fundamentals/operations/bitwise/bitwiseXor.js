{
    let num1 = 5;
    let num2 = 10;
    console.log("Bitwise XOR of", num1, "and", num2, "is:", num1 ^ num2);

    let a = 1; // In binary: 0001
    let b = 2;
    console.log("Bitwise XOR of", a, "and", b, "is:", a ^ b); // Output: 3 (In binary: 0011)

    let x = 12;
    let y = 5;  // In binary: 0101
    console.log("Bitwise XOR of", x, "and", y, "is:", x ^ y); // Output: 9 (In binary: 1001)

    let str1 = "Hello";
    let str2 = "World";
    console.log("Bitwise XOR of strings 'Hello' and 'World' is:", str1 ^ str2); // Output: 0

    let boolTrue = true;
    let boolFalse = false;
    console.log("Bitwise XOR of true and false is:", boolTrue ^ boolFalse);

    let floatNum1 = 5.7;
    let floatNum2 = 2.3;
    console.log("Bitwise XOR of floats 5.7 and 2.3 is:", floatNum1 ^ floatNum2); // Output: 7

}